<h2>Varför Vision Njurunda?</h2>
<p>
Njurunda är en del i Sundsvalls kommun och vi som bor i den södra delen av kommunen vill gärna vara med i planeringen för hur Sundsvall ska se ut i framtiden. En ny väg är snart klar som
kommer att passera i utkanten av Njurunda, Nolby, Hemmanet med start i Myre.

<p>
I och med den nya E4:an har konceptet ”En väg 1000 möjligheter” myntats. En fin och hoppfull fras som ger hopp om utveckling och skapande av förutsättningar för nyetableringar i vårt närområde. Om man inte tänker längre än till frasens slut så är frasen hoppfull, men om man är visionär och har idéer om vad man kan göra för bygden nu när E4 byggs, hur gör man då? Vart vänder man sig? Är det någon som kommer lyssna på Dig och vad du kan ha för idéer om Njurundas framtida utveckling? Det är ju ingen som direkt frågar oss vad vi tycker...

<p>
Sundsvalls kommun smider planer för framtiden och ibland känns det som vi som bor inom kommunen inte alltid får ta del av dessa. I planeringen ligger inte bara planer för hur Stenstan om kringområdet centralt ska se ut, de planeras också för hur Njurunda ska se ut.

<p>
Vi får information ja, men kanske inte inom alla områden. Vi skulle vilja att allt som rör vår bygd och närområde ska vara tillgängligt för Njurundaborna på ett tydligt sätt. Vi medborgare i Njurunda vill gärna också vara en del i planeringen för hur Sundsvall ska se ut i framtiden och i synnerhet hur Njurunda formas.

<p>
Idag finns det inte direkt någon grupp som värnar om utvecklingen i vårt närområde och vi vill genom Vision Njurunda visa på att det finns ett stort intresse bland både förtagare/ entreprenörer och medborgare att dels få information och upplysning om vad som planeras, dels ge möjighet för lokala visionärer att få komma till tals och vara delaktiga i den framtida samhällsutvecklingen och få vara med på tåget av att lyfta och utveckla Njurunda. Vi ska ta tillvara på Njurundas förutsättningar för att skapa nyetableringar, arbeten och trivsel.

<p>
Tanken är att Vision Njurunda ska vara länken mellan beslutsfattarna inom kommunen och de boende i Njurunda.
<p><b>NjurundaFöretagarna</b>

<p>
